#include <stdio.h>
int main()
{
	float I,x;
	printf("请输入当月利润:");
	scanf("%f",&I);
	if(I<=100000){
		x=I*0.1;
	}else if(I<=200000){
		x=10000+(I-100000)*0.075;
	}else if(x<=400000){
		x=10000+7500+(I-200000)*0.05;
	}else if(I<=600000){
		x=10000+7500+10000+(I-400000)*0.03;
	}else if(I<=1000000){
		x=10000+7500+10000+6000+(I-600000)*0.015;
	}else{
		x=10000+7500+10000+6000+6000+(I-1000000)*0.01;
	}
	printf("当月应发奖金为:%.2f\n",x);
	return 0;
} 
